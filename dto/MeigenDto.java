package jp.kelonos.dto;

public class MeigenDto {
	private int meigenId;
	private String body;
	private String author;
	
	public int getMeigenId() {
		return meigenId;
	}
	public void setMeigenId(int meigenId) {
		this.meigenId = meigenId;
	}
	public String getBody() {
		return body;
	}
	public void setBody(String body) {
		this.body = body;
	}
	public String getAuthor() {
		return author;
	}
	public void setAuthor(String author) {
		this.author = author;
	}

}
